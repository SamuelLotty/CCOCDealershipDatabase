
public class Stock {
	private int ItemNo,QuantStock,QuantOrder;
	private String ItemName;
	
	public void Stock() {
	}
	public int getItemNo() {
		return ItemNo;
	}
	public void setItemNo(int ItemNo) {
		this.ItemNo = ItemNo;
	}
	public int getQuantStock() {
		return QuantStock;
	}
	public void setQuantStock(int QuantStock) {
		this.QuantStock = QuantStock;
	}
	public int getQuantOrder() {
		return QuantOrder;
	}
	public void setQuantOrder(int QuantOrder) {
		this.QuantOrder=QuantOrder;
	}
	public String getItemName() {
		return ItemName;
	}
	public void setItemName(String ItemName) {
		this.ItemName=ItemName;
	}
	
}
